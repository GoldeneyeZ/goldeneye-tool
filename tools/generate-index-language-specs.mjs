import { execFileSync } from "node:child_process";
import { readFileSync } from "node:fs";
import { resolve } from "node:path";

const workspace = resolve(import.meta.dirname, "..");
const upstream = resolve(workspace, ".upstream/codebase-memory-mcp");
const specsPath = resolve(upstream, "internal/cbm/lang_specs.c");
const generatedGrammarsPath = resolve(
  workspace,
  "crates/adapters/goldeneye-full-grammars/src/generated.rs",
);

const specsSource = readFileSync(specsPath, "utf8");
const generatedGrammars = readFileSync(generatedGrammarsPath, "utf8");
const upstreamCommit = execFileSync(
  "git",
  ["-C", upstream, "rev-parse", "HEAD"],
  { encoding: "utf8" },
).trim();

const specsWithoutComments = specsSource
  .replace(/\/\*[\s\S]*?\*\//g, "")
  .replace(/\/\/.*$/gm, "");

const arrays = new Map();
for (const match of specsWithoutComments.matchAll(
  /static const char \*(\w+)\[\]\s*=\s*\{([\s\S]*?)\};/g,
)) {
  arrays.set(
    match[1],
    [...match[2].matchAll(/"([^"]*)"/g)].map((value) => value[1]),
  );
}

const availableIds = new Set();
for (const match of generatedGrammars.matchAll(
  /GeneratedLanguage \{ id: "([^"]+)", availability: GeneratedAvailability::(Available|Unavailable)/g,
)) {
  if (match[2] === "Available") availableIds.add(match[1]);
}

const fieldDefinitions = [
  ["function_kinds", 1],
  ["class_kinds", 2],
  ["field_kinds", 3],
  ["module_kinds", 4],
  ["call_kinds", 5],
  ["import_kinds", 6],
  ["import_from_kinds", 7],
  ["branch_kinds", 8],
  ["variable_kinds", 9],
  ["assignment_kinds", 10],
  ["throw_kinds", 11],
  ["decorator_kinds", 13],
];

function splitInitializers(body) {
  const values = [];
  let start = 0;
  let depth = 0;
  let quote = null;
  let escaped = false;
  for (let index = 0; index < body.length; index += 1) {
    const character = body[index];
    if (quote) {
      if (escaped) escaped = false;
      else if (character === "\\") escaped = true;
      else if (character === quote) quote = null;
      continue;
    }
    if (character === '"' || character === "'") quote = character;
    else if ("({[".includes(character)) depth += 1;
    else if (")}]".includes(character)) depth -= 1;
    else if (character === "," && depth === 0) {
      values.push(body.slice(start, index).trim());
      start = index + 1;
    }
  }
  values.push(body.slice(start).trim());
  return values.filter(Boolean);
}

function parseRows(source) {
  const rows = [];
  const starts = /^\s*\[CBM_LANG_(\w+)\]\s*=\s*\{/gm;
  for (const match of source.matchAll(starts)) {
    const open = match.index + match[0].lastIndexOf("{");
    let depth = 1;
    let quote = null;
    let escaped = false;
    let end = open + 1;
    for (; end < source.length && depth > 0; end += 1) {
      const character = source[end];
      if (quote) {
        if (escaped) escaped = false;
        else if (character === "\\") escaped = true;
        else if (character === quote) quote = null;
      } else if (character === '"' || character === "'") quote = character;
      else if (character === "{") depth += 1;
      else if (character === "}") depth -= 1;
    }
    if (depth !== 0) throw new Error(`unterminated spec row ${match[1]}`);
    rows.push([match[1], source.slice(open + 1, end - 1)]);
  }
  return rows;
}

function valuesFor(reference, language, field) {
  if (!reference || reference === "NULL" || reference === "empty_types") return [];
  if (reference.includes("{")) {
    return [...reference.matchAll(/"([^"]*)"/g)].map((value) => value[1]);
  }
  const values = arrays.get(reference);
  if (!values) {
    throw new Error(`missing ${field} array ${reference} for ${language}`);
  }
  return values;
}

const specs = [];
for (const [enumName, body] of parseRows(specsWithoutComments)) {
  const id = enumName.toLowerCase();
  const fields = splitInitializers(body);
  const spec = { id };
  for (const [name, index] of fieldDefinitions) {
    spec[name] = valuesFor(fields[index], id, name);
  }
  specs.push(spec);
}
specs.sort((left, right) => left.id.localeCompare(right.id));

const specIds = new Set(specs.map((spec) => spec.id));
const missing = [...availableIds].filter((id) => !specIds.has(id));
const unavailable = [...specIds].filter((id) => !availableIds.has(id));
if (specs.length !== 159 || missing.length || unavailable.length) {
  throw new Error(
    `registry mismatch: specs=${specs.length}, missing=${missing}, unavailable=${unavailable}`,
  );
}

function rustSlice(values) {
  return `&[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

const output = [];
output.push("// @generated by tools/generate-index-language-specs.mjs; do not edit manually.");
output.push(`// Audited upstream: DeusData/codebase-memory-mcp@${upstreamCommit}`);
output.push("");
output.push("#[derive(Debug, Clone, Copy)]");
output.push("pub(crate) struct LanguageSpec {");
output.push("    pub id: &'static str,");
for (const [name] of fieldDefinitions) {
  output.push(`    pub ${name}: &'static [&'static str],`);
}
output.push("}");
output.push("");
output.push("#[cfg(test)]");
output.push("pub(crate) const AUDITED_LANGUAGE_SPEC_COUNT: usize = LANGUAGE_SPECS.len();");
output.push("");
output.push("pub(crate) fn language_spec(language_id: &str) -> Option<&'static LanguageSpec> {");
output.push("    LANGUAGE_SPECS");
output.push("        .binary_search_by(|spec| spec.id.cmp(language_id))");
output.push("        .ok()");
output.push("        .and_then(|index| LANGUAGE_SPECS.get(index))");
output.push("}");
output.push("");
output.push("static LANGUAGE_SPECS: &[LanguageSpec] = &[");
for (const spec of specs) {
  output.push("    LanguageSpec {");
  output.push(`        id: ${JSON.stringify(spec.id)},`);
  for (const [name] of fieldDefinitions) {
    output.push(`        ${name}: ${rustSlice(spec[name])},`);
  }
  output.push("    },");
}
output.push("];");
output.push("");
output.push("#[cfg(test)]");
output.push("mod tests {");
output.push("    use super::{AUDITED_LANGUAGE_SPEC_COUNT, LANGUAGE_SPECS, language_spec};");
output.push("");
output.push("    #[test]");
output.push("    fn registry_is_complete_sorted_and_matches_callable_pack() {");
output.push("        assert_eq!(AUDITED_LANGUAGE_SPEC_COUNT, 159);");
output.push("        assert!(LANGUAGE_SPECS.windows(2).all(|pair| pair[0].id < pair[1].id));");
output.push('        assert!(language_spec("nim").is_none());');
output.push('        assert!(language_spec("rust").is_some());');
output.push('        assert!(language_spec("mojo").is_some());');
output.push("    }");
output.push("}");

process.stdout.write(`${output.join("\n")}\n`);
